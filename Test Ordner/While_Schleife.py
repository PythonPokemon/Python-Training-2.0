# unendlichkeutsschleife
# der code wird endlos ausgeführt da 1 kleier 10 ist = also True, Richtig!

zähler = 1 
while zähler < 10: 
    print("Der Code wird endlos widerholt")
    zähler += 1 
# diese funktion zählt immer +1, bedeutet also ab 1 ++++++++++ = 10, 
# wenn die schleife das 10te mal die schleifen durchlaufen hat wird die schliefe beendet
# bedeutet im umkehrschluss das ohne diesen zähkler die schleife endloss wäre